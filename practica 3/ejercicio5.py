muñecas = int(input("Ingresa la cantidad de muñecas vendidas: "))
payasos = int(input("Ingresa la cantidad de payasos vendidos: "))

pesoMuñeca = int(112)
pesoPayaso = int(75)

muñecasResultado = muñecas * pesoMuñeca
payasosResultado = payasos * pesoPayaso

print("El peso de las muñecas es:", muñecasResultado)
print("El peso de los payasos es:", payasosResultado)